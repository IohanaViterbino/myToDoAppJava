package com.example.mytodoapp;

public class Task {
    private Integer Id;
    private String Texto;

    public Integer getId() {
        return Id;
    }

    public String getTexto() {
        return Texto;
    }

    public void setId(Integer id) {
        Id = id;
    }

    public void setTexto(String texto) {
        Texto = texto;
    }


}